#include <string>
#include <stack>

using namespace std;

//leetcode submit region begin(Prohibit modification and deletion)
class Solution {
public:
    bool isValid(string s) {
        stack<char> Stack;
        for (auto Char : s)
        {
            if (Char == '(' || Char == '[' || Char == '{')
            {
                Stack.push(Char);
            }
            else
            {
                if (Stack.empty())
                {
                    return false;
                }
                
                if (Char == ')')
                {
                    if (Stack.top() != '(')
                    {
                        return false;
                    }
                    Stack.pop();
                }
                else if (Char == ']')
                {
                    if (Stack.top() != '[')
                    {
                        return false;
                    }
                    Stack.pop();
                }
                else
                {
                    if (Stack.top() != '{')
                    {
                        return false;
                    }
                    Stack.pop();
                }
            }
        }
        return Stack.empty();
    }
};
